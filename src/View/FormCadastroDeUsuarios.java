    private void limparCampos() {
        txtNome.setText("");
        txtMatricula.setText("");
        txtSenha.setText("");
        txtNome.requestFocus();
    }

    private boolean validarCampos() {
        String validacao = "";
        if (txtNome.getText().equals("")) {
            validacao += "O nome não foi preenchido!\n";
        }
        if (txtMatricula.getText().equals("")) {
            validacao += "A matrícula não foi preenchida!\n";
        } else {
            try {
                Integer.parseInt(txtMatricula.getText());
            } catch (Exception e) {
                validacao += "A matrícula deve conter apenas com números!\n";
            }
        }
        if (txtSenha.getText().equals("")) {
            validacao += "A senha não foi preenchida!";
        }
        if (!"".equals(validacao)) {
            JOptionPane.showMessageDialog(this, validacao);
            limparCampos();
            return false;
        } else {
            return true;
        }
    }

