    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        boolean campos = false;
        campos = validarCampos();
        if (campos == true) {
            Usuario u = new Usuario();
            u.setNome(txtNome.getText());
            u.setMatricula(Integer.parseInt(txtMatricula.getText()));
            try {
                u.setSenha(txtSenha.getText());
            } catch (NoSuchAlgorithmException ex) {
                Logger.getLogger(FormCadastroDeUsuarios.class.getName()).log(Level.SEVERE, null, ex);
            }
            try {
                this.cu.criarUsuario(u);
                if (this.cu.getErro().equals("Erro ao criar usuário: Duplicate entry '152138' for key 'PRIMARY'")) {
                    JOptionPane.showMessageDialog(this, "Usuário já existe!");
                    limparCampos();
                } else {
                    JOptionPane.showMessageDialog(this, this.cu.getErro());
                    limparCampos();
                    dispose();
                }
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(this, this.cu.getErro() + "\nErro de IO: " + ex);
            }
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

